export type TranslationValues = Record<string, string | number>;

export function translate(locale: Locale, key: string, values: TranslationValues = {}): string {
  const template = locale === "ar" ? ar[key] ?? key : key;
  return Object.entries(values).reduce((result, [name, value]) => result.replaceAll(`{${name}}`, String(value)), template);
}

export function formatDateForLocale(locale: Locale, value: string | Date): string {
  return new Intl.DateTimeFormat(locale === "ar" ? "ar-SA" : "en-US", { year: "numeric", month: "short", day: "numeric" }).format(new Date(value));
}

